#基本資料型態 & 變數

#字串 "" or ''

#數字 直接打

#boolean True or Flase

# .lower() >小寫
phrase ="hello jay"
print(phrase.lower())

# .upper() >大寫
phrase ="hello jay"
print(phrase.upper())

# .isupper() .islower()>判斷大小寫
phrase ="Hello jay"
print(phrase.isupper())

#[]>字元位置
phrase ="Hello jay"
print(phrase[1])

#.index>找位置
phrase ="Hello jay"
print(phrase.index("H"))

#.replce("a","b") a全部替換成b
phrase ="Hello jay"
print(phrase.replace("l","j"))